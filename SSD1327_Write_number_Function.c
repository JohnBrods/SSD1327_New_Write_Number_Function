void Set_Display_Number_Font(unsigned char X_Position, unsigned char Y_Position){
     unsigned char End_Column;
     unsigned char Set_End_Column = X_Position + (Number_Font_width/2) ; //(bytes -1 bit) 8 pixels = 1 byte, 16 pixels = 2 bytes
     unsigned char Set_End_Row = Y_Position + Number_font_height;
     End_Column = Set_End_Column;

     TFT_CS = 0;
     TFT_Set_Index_SSD1327(0x15);                 // Set Column Address
     TFT_Write_Command_SSD1327(X_Position); // Start Column Address   bytes
     TFT_Write_Command_SSD1327(Set_End_Column);   // End Column Address   bytes or pixels of data

     TFT_Set_Index_SSD1327(0x75);                 // Set Row Address (segment)
     TFT_Write_Command_SSD1327(Y_Position);    // Satrt Row Address
     TFT_Write_Command_SSD1327(127);              // End Row Address
     TFT_CS = 1;
     
}



